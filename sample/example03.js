

function plus(p1, p2) {
    return p1 + p2; // p1, p2 합연산의 결과를 반환
}

function minus(p1, p2) {
    return p1 - p2; // p1, p2 뺄셈연산의 결과를 반환
}

function multi(p1, p2) {
    return p1 * p2; // p1, p2 곱연산의 결과를 반환
}

function div(p1, p2) {
    return p1 / p2; // p1, p2 나눗셈연산의 결과를 반환
}

console.log(plus(2, 3));
console.log(minus(2, 3));
console.log(multi(2, 3));
console.log(div(2, 3));